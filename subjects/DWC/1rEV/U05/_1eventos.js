//* Eventos del Mouse:
// click: Cuando hace click en un elemento.
// contextmenu: Cuando se hace clic derecho sobre un elemento.
// mousover: Cuando el ratón está sobre un elemento.
// mouseout: Cuando el ratón deja de estar sobre un elemento.
// mousedown: Cuando se mantiene sobre un elemento.
// mouseup: Cuadno se deja de mantener.
// mousemove: Cuando se mueve el ratón.

//* Eventos del teclado:
// keydown: Cuando se presiona una tecla.
// keyup: Cuando se suelta una tecla.
// keypress: Mientras se mantiene una tecla presionada.

//* Eventos de elementos de formulario
// submit: Cuando el usuario envía a <form>.
// reset: Cuando borramos el contenido de un formulario.
// focus: Cuando un visitante se centra en un elemento.
// change: Cuando cambia el valor de algún elemento.

//* Eventos DOM:
// load: Cuando se carga y procesas el HTML
// resize: Cuando cambia el tamaño de la ventana del navegador.
// scroll: Cuando se desplaza la página.